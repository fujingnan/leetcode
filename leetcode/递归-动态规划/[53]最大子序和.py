class Solution:
    def maxSubArray(self, nums: List[int]) -> int:
        n = len(nums)
        dp = nums
        for i in range(1, n):
            dp[i] = max(dp[i], dp[i - 1] + nums[i])
        # exm: [-2,1,-3,4,-1,2,1,-5,4]
        # dp: [-2, 1, -2, 4, 3, 5, 6, 1, 5]
        return max(dp)

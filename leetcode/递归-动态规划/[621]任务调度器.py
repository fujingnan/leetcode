class Solution:
    def leastInterval(self, tasks: List[str], n: int) -> int:
        from collections import Counter
        tasks_map = Counter(tasks)
        tasks_sort = sorted(tasks_map.items(), key=lambda x:x[1], reverse=True)
        max_count_task = tasks_sort[0][1]
        res = (max_count_task - 1) * (n + 1)
        for sort in tasks_sort:
            if sort[1] == max_count_task:
                res += 1
        return res if res >= len(tasks) else len(tasks)

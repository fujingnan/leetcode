class Solution:
    def jump(self, nums: List[int]) -> int:
        maxdist, end, res = 0, 0, 0
        for i in range(len(nums)-1):
            maxdist = max(maxdist, i + nums[i])
            if i == end:
                end = maxdist
                res += 1
        return res

